def download_one(symbol, name):
    """下载单只股票"""
    try:
        df = ak.stock_zh_a_hist(
            symbol=symbol,
            period="daily", 
            start_date="20230101",
            end_date=datetime.now().strftime("%Y%m%d"),
            adjust=""
        )
        if df is not None and len(df) > 200:
            df.to_csv(f"{CACHE_DIR}/{symbol}.csv", index=False)
            return True
    except Exception as e:
        print(f"  错误: {e}")
    return False

def main():
    os.makedirs(CACHE_DIR, exist_ok=True)
    
    print(f"准备下载 {len(CORE_STOCKS)} 只核心股票...")
    
    success = 0
    failed = []
    
    for i, (symbol, name) in enumerate(CORE_STOCKS):
        print(f"[{i+1}/{len(CORE_STOCKS)}] {symbol} {name}...", end=" ")
        
        if download_one(symbol, name):
            print("✓")
            success += 1
        else:
            print("✗")
            failed.append((symbol, name))
        
        time.sleep(1)  # 避免限流
    
    print(f"\n=== 完成 ===")
    print(f"成功: {success}/{len(CORE_STOCKS)}")
    
    if failed:
        print(f"失败: {failed}")

if __name__ == "__main__":
    main()
